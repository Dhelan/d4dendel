			<h2>Symbols of God’s Word</h2>
			<p>The Word of God is the Living Word. It carries with it the power to accomplish the things that it declares. Human words can speak of what is, but God speaks of things that are not yet done and then creates them by the power of His word. The Word of God is a creative Word. The audible Word that proceeds from His mouth has the power to create everything that it proclaims.</p>

			<p>In the Creation story of Genesis 1, the expression “God said” is used repeatedly (Gen. 1:3, 6, 11, 14, 20, 24, 26, 29). God’s declarative words had such power that when He spoke, dry land appeared, plants sprouted, flowers blossomed, fruit trees flourished, and animals sprang forth.</p>

			<p>There is a fascinating Hebrew word used in Genesis 1 for the creative activity of God. It is the word bara. In this particular form, it is used of God’s activity to create something from nothing. The verb is used only when God is the subject. That is, only God alone can bara, and He does so through the power of His spoken word.</p>

			<p>God not only created this world through the power of His word, but He sustains and upholds it through His word, as well. The same power that is in the spoken Word of God is in His written word. The same Holy Spirit that was active in Creation was active in inspiring Scripture. He is present when we read the Bible or share it with others. There is life-giving, life-changing, creative power in the Word of God. “The creative energy that called the worlds into existence is in the word of God. This word imparts power; it begets life. Every command is a promise; accepted by the will, received into the soul, it brings with it the life of the Infinite One. It transforms the nature and re-creates the soul in the image of God”. — Ellen G. White, Education, p. 126.</p>

			<p>As we personally grasp the promises found in the Word of God, our lives are changed, and as we help others to grasp these amazing promises, the Holy Spirit will change their lives, too.

			<b>Imagine: God spoke and it was. How can we grasp what this means? What does this amazing reality tell us about His power? Why should this truth about God’s creative power be comforting to us?</b></p>
